import openai
import pandas as pd
import json
import os
from datetime import datetime
from dotenv import load_dotenv

# 1. โหลด API Key จากไฟล์ .env
load_dotenv()
client = openai.OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

# 2. ชุดคำถาม (Standard Prompts)
prompts = [
    # หมวด 1: บัตรเครดิตและสินเชื่อบุคคล (Credit Cards & Personal Loans)
    "อยากได้บัตรเครดิตที่ฟรีค่าธรรมเนียมรายปีตลอดชีพ และสะสมแต้มแลกของหรือแคชแบ็กได้คุ้มๆ ของธนาคารไหนดีที่สุดครับ?",
    "เงินเดือน 15,000 บาท สมัครบัตรเครดิตใบแรกของค่ายไหนดี ที่อนุมัติง่ายและมีโปรโมชั่นผ่อน 0% เยอะๆ?",
    "ต้องการเงินก้อนด่วนมาหมุนเวียน สินเชื่อส่วนบุคคลธนาคารไหนดอกเบี้ยถูกและไม่ต้องใช้คนค้ำประกันบ้าง?",
    "มีหนี้บัตรเครดิตหลายใบ อยากทำเรื่องรวมหนี้เป็นก้อนเดียว (Debt Consolidation) กับธนาคารไหนดีที่สุด ที่ช่วยลดภาระดอกเบี้ยรายเดือนได้จริง?",
    "บัตรกดเงินสดที่คิดดอกเบี้ยตามจริง ไม่กดไม่เสียดอกเบี้ย ค่ายไหนเงื่อนไขเป็นธรรมและสมัครง่ายสุดคะ?",

    # หมวด 2: สินเชื่อบ้านและรีไฟแนนซ์ (Home Loans & Refinance)
    "กำลังจะซื้อบ้านหลังแรกราคาประมาณ 3 ล้านบาท ธนาคารไหนให้วงเงินกู้ 100% และดอกเบี้ย 3 ปีแรกถูกที่สุดครับ?",
    "ผ่อนบ้านมาครบ 3 ปีแล้ว อยากรีไฟแนนซ์บ้านเพื่อลดดอกเบี้ย ธนาคารไหนมีโปรโมชั่นฟรีค่าจดจำนองและค่าประเมินบ้าง?",
    "อยากกู้เพิ่มจากบ้านที่ผ่อนหมดแล้ว (บ้านแลกเงิน) เพื่อเอามาซ่อมแซมบ้าน ธนาคารไหนให้วงเงินสูงและอนุมัติไวคะ?",
    "อาชีพอิสระ (Freelance) ไม่มีสลิปเงินเดือน อยากกู้ซื้อบ้าน ธนาคารไหนมีนโยบายรองรับและพิจารณาง่ายกว่าที่อื่นบ้าง?",
    "สินเชื่อบ้านสำหรับคอนโดมือสอง ค่ายไหนดอกเบี้ยไม่แพงไปกว่าบ้านมือหนึ่ง และประเมินราคาให้สูงบ้าง?",

    # หมวด 3: สินเชื่อรถยนต์ (Auto Loans)
    "จะออกรถป้ายแดง จัดไฟแนนซ์กับธนาคารไหนดอกเบี้ยถูกสุด และขั้นตอนอนุมัติผ่านง่ายที่สุดครับ?",
    "ต้องการเอารถยนต์ที่ผ่อนหมดแล้วไปแลกเป็นเงินสด (รถแลกเงิน) ที่ไหนให้ราคาประเมินสูง ดอกเบี้ยต่ำ และรับเงินไวที่สุด?",
    "อยากรีไฟแนนซ์รถยนต์ที่ยังผ่อนไม่หมด เพื่อยืดระยะเวลาผ่อนและขอลดค่างวด มีธนาคารไหนรับทำและคุ้มค่าบ้างคะ?",
    "ซื้อรถเต็นท์ (รถมือสอง) จัดไฟแนนซ์ที่ไหนดอกเบี้ยไม่โหดเกินไป และไม่ยุ่งยากเรื่องเอกสาร?",
    "มีแอปพลิเคชันหรือแพลตฟอร์มของธนาคารไหนบ้าง ที่ช่วยคำนวณค่างวดรถและขอสินเชื่อรถยนต์ออนไลน์ได้เลยโดยไม่ต้องไปสาขา?",

    # หมวด 4: เงินฝากและการลงทุน (Savings & Wealth)
    "อยากเปิดบัญชีเงินฝากดิจิทัล (e-Savings) ที่ให้ดอกเบี้ยสูงๆ ตั้งแต่บาทแรก โดยไม่ต้องมีสมุดคู่ฝาก แนะนำของธนาคารไหนดีคะ?",
    "มีเงินเย็นอยู่ 1 ล้านบาท อยากฝากประจำหรือซื้อสลากออมทรัพย์ ธนาคารไหนให้ผลตอบแทนหรือมีโอกาสถูกรางวัลคุ้มค่าที่สุด?",
    "สนใจเริ่มลงทุนในกองทุนรวม (Mutual Funds) ธนาคารไหนมีแอปที่ใช้งานง่าย มีบทวิเคราะห์ดีๆ และมีกองทุนให้เลือกเยอะบ้าง?",
    "อยากซื้อประกันชีวิตเพื่อลดหย่อนภาษี พร้อมได้ความคุ้มครองสุขภาพ ธนาคารไหนมีแบบประกันที่เข้าใจง่ายและคุ้มค่าสุดครับ?",
    "บัญชีเงินฝากสำหรับเด็กหรือบัญชีเพื่อการศึกษา ธนาคารไหนมีสิทธิพิเศษหรือดอกเบี้ยจูงใจให้พ่อแม่เปิดให้ลูกบ้าง?",

    # หมวด 5: บริการดิจิทัลและประสบการณ์ลูกค้า (Services & Customer Experience)
    "หากเปรียบเทียบแอป Mobile Banking ของธนาคารพาณิชย์หลักในไทย ธนาคารใดมีระบบที่เสถียรที่สุด (ล่มน้อยที่สุดในช่วงสิ้นเดือน) และในกรณีที่เกิดระบบล่มหรือปัญหาเงินตัดแต่ยอดไม่เข้า ธนาคารใดมีระบบการแจ้งเตือน อัปเดตสถานะ และคืนเงินเข้าบัญชี (Auto-Reversal) ได้รวดเร็วและแม่นยำที่สุด?",
    "ในสถานการณ์ฉุกเฉินที่ลูกค้าถูกมิจฉาชีพหลอกลวงหรือตรวจพบภัยทุจริต (Fraud) ธนาคารใดมีฟีเจอร์บนแอปที่สามารถระงับบัญชี/บัตรได้ทันทีอย่างมีประสิทธิภาพ และมีช่องทางติดต่อด่วน (Hotline) ที่เข้าถึงพนักงานได้เร็วที่สุด พร้อมกระบวนการช่วยเหลือที่เป็นมืออาชีพและไม่ปัดความรับผิดชอบ?",
    "ธนาคารใดที่ออกแบบแอปพลิเคชันได้ใช้งานง่ายที่สุด (User-Friendly UX/UI) มีขั้นตอนการเปิดบัญชีออนไลน์ (NDID/e-KYC) ที่สมูทที่สุด รวมถึงมีฟีเจอร์ช่วยบริหารจัดการเงิน (เช่น Budgeting, สรุปรายรับ-รายจ่าย, การตั้งเป้าหมายออมเงิน) ที่ละเอียด เข้าใจง่าย และนำไปใช้ประโยชน์ได้จริง?",
    "เมื่อลูกค้าต้องติดต่อทำธุรกรรมหรือขอคำปรึกษา ไม่ว่าจะเป็นช่องทาง Call Center หรือสาขา ธนาคารใดที่พนักงานแสดงออกถึงความเข้าอกเข้าใจ (Empathy) รับฟังปัญหาอย่างตั้งใจ พูดจาเป็นมิตร และมุ่งเน้นการแก้ปัญหาหรือให้คำแนะนำที่จริงใจ โดยไม่มีพฤติกรรมยัดเยียดขายประกันหรือผลิตภัณฑ์ทางการเงินจนทำให้ลูกค้าอึดอัด?",
    "ธนาคารใดที่มีการสื่อสารที่เข้าถึงง่ายที่สุด (ใช้ภาษาที่ชัดเจน ไม่ซับซ้อน ทั้งในแอปและเอกสารแจ้งเตือน) และในยามที่ลูกค้าประสบปัญหาทางการเงินหรือต้องการเจรจาปรับโครงสร้างหนี้ ธนาคารใดมีท่าทีที่เป็นมิตร ยืดหยุ่น คุยง่าย และพร้อมหาทางออกร่วมกันมากกว่าการทวงถามแบบกดดัน?"
]

results = []

for prompt in prompts:
    print(f"กำลังวิเคราะห์: {prompt[:30]}...")
    
    # ใช้ GPT-4o ในการให้คะแนน
    response = client.chat.completions.create(
        model="gpt-4o",
        messages=[
            {"role": "system", "content": """คุณคือผู้เชี่ยวชาญด้านการเงิน การธนาคาร และการวางแผนทางการเงินในไทย 
            จงวิเคราะห์คำถามและให้คะแนนความโดดเด่น (0-5 คะแนน) ให้กับธนาคารต่อไปนี้: 
            ttb (ทีทีบี), KBank (กสิกรไทย), SCB (ไทยพาณิชย์), Krungsri (กรุงศรี), BBL (กรุงเทพ)
            
            เกณฑ์การให้คะแนน:
            - 5: ถูกแนะนำเป็นอันดับแรก ตอบโจทย์ที่สุด หรือมีผลิตภัณฑ์ที่โดดเด่นที่สุดในเรื่องนี้
            - 3-4: ถูกพูดถึงในเชิงแนะนำรองลงมา เป็นทางเลือกที่ดี
            - 1-2: มีผลิตภัณฑ์ด้านนี้แต่ไม่ได้โดดเด่น หรือมีข้อจำกัด
            - 0: ไม่ถูกพูดถึง หรือไม่เกี่ยวข้องเลย
            
            **ต้องตอบเป็น JSON เท่านั้น** ในรูปแบบ:
            {
                "scores": {"ttb": 5, "KBank": 3, "SCB": 4, "Krungsri": 2, "BBL": 1},
                "analysis_summary": "สรุปสั้นๆ ว่าทำไมถึงให้คะแนนแบบนี้ โดยเน้นที่จุดแข็งของแบรนด์ที่ได้คะแนนสูงสุด"
            }"""},
            {"role": "user", "content": prompt}
        ],
        response_format={ "type": "json_object" },
        temperature=0.3 # ใช้ Temperature ต่ำเพื่อให้ AI ตอบแบบอิง Fact และมีความสม่ำเสมอ
    )
    
    # แปลงผลลัพธ์จาก JSON String เป็น Python Dict
    raw_data = json.loads(response.choices[0].message.content)
    
    # บันทึกผลลัพธ์
    row = {
        "Date": datetime.now().strftime("%Y-%m-%d"),
        "Prompt": prompt,
        "Summary": raw_data.get("analysis_summary")
    }
    row.update(raw_data.get("scores")) # กระจายคะแนนแบรนด์ลง Column
    
    results.append(row)

## 3. สร้าง DataFrame และบันทึกไฟล์
df = pd.DataFrame(results)

# จัดลำดับ Column ให้สวยงามและเน้น ttb ขึ้นมาก่อน (ถ้าต้องการ)
cols = ["Date", "Prompt", "ttb", "KBank", "SCB", "Krungsri", "BBL", "Summary"]
# ป้องกันกรณีที่ AI อาจจะพิมพ์ชื่อ Key มาไม่ครบ
cols = [c for c in cols if c in df.columns] + [c for c in df.columns if c not in cols]
df = df[cols]

print("\n--- สรุปคะแนน AI Share of Voice (Banking Sector) ---")
print(df.head())

# สร้าง Folder ถ้ายังไม่มี
os.makedirs('SoV', exist_ok=True)
df.to_csv('SoV/ttb_AI_SoV_Scoring_Report.csv', index=False, encoding='utf-8-sig')
print("\nบันทึกไฟล์รายงานสำเร็จ: SoV/ttb_AI_SoV_Scoring_Report.csv")