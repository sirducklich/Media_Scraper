import openai
import pandas as pd
import json
import os
from datetime import datetime
from dotenv import load_dotenv

# 1. โหลด API Key จากไฟล์ .env
load_dotenv()
client = openai.OpenAI(api_key=os.getenv("OPENAI_API_KEY"))

